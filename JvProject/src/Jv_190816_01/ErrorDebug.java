public class ErrorDebug {
    public static void main(String[] args) {
        int a, b, c;

        a = 3;
        b = 20;

        c = a + b;
        System.out.println("a + b : " + c);

        c = a - b;
        System.out.println("a - b : " + c);

        c = a / b;
        System.out.println("a / b : " + c);
    }
}